					</div>
					<div style="width:100%;">
						<img class="fac_img" src="images/facilities/water.jpg" alt="Water">
					</div>
					<div style="padding:20px;">
						<p>A Slovakian proverb says, "Pure water is the world’s first and
							foremost medicine." The Institute ensures that the drinking water
							facilities on the campus are maintained at regular intervals and with
							utmost care.</p><br>
						<p>Drinking water supplied by the Mysore city corporation is available
							throughout the campus. All drinking water outlets and water coolers
							are fitted with top-notch water purifiers. All underground and
							overhead water tanks are cleaned by professional service-providers
							thrice a year using sophisticated equipment including UV equipment.
							Further, bottled water is also available at the food outlets on the
							campus.</p>
					</div>
