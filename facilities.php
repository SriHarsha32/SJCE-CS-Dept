							resulting in an ‘always on, always connected’ society.</p><br>
						<p>Wi-Fi provides a consistent, high quality connected experience,
							regardless of environmental factors. Wi-Fi setup for the entire
							Institute is controlled from the Data Centre through firewall.</p><br>
						<p>Wi-Fi setup has over 30 access points strategically located across the
							Campus for access to Internet services. Wi-Fi is authentication driven
							and is with restrictions as per user level for maximum security.</p>
